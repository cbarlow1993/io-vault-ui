/**
 * Get list of chain aliases that have an active overall status (Alpha, Beta, or GA)
 */
export const getActiveSupportedChains = (): ChainAlias[] => {
  return Object.entries(ALL_CHAINS)
    .filter(([_, chainConfig]) =>
      activeFeatureStatuses.includes(chainConfig.features[ChainFeatures.OVERALL_STATUS])
    )
    .map(([chainAlias]) => chainAlias as ChainAlias);
};

/**
 * Zod enum schema for supported chains (chains with active overall status: Alpha, Beta, or GA)
 * This is the single source of truth for chain validation across the application
 */
export const supportedChains = z.enum(
  getActiveSupportedChains() as [ChainAlias, ChainAlias, ...ChainAlias[]]
);

/**
 * Get the iofinnet RPC URL for a chain alias.
 * Uses the pattern: https://nodes.iofinnet.com/internal/{chainAlias}
 */
export function getRpcUrl(chainAlias: ChainAlias): string {
  return `https://nodes.iofinnet.com/internal/${chainAlias}`;
}

/**
 * Get RPC URLs for all active EVM chains.
 * Returns a map of chainAlias -> RPC URL
 */
export function getEvmRpcUrls(): Record<string, string> {
  const evmAliases = Object.keys(evmChainWithFeatures) as EvmChainAlias[];
  const activeEvmChains = evmAliases.filter((alias) =>
    activeFeatureStatuses.includes(evmChainWithFeatures[alias].features[ChainFeatures.OVERALL_STATUS])
  );

  return activeEvmChains.reduce(
    (acc, alias) => {
      acc[alias] = getRpcUrl(alias);
      return acc;
    },
    {} as Record<string, string>
  );
}

